package com.example.hornsby.spacegame;

public class Astroid {
    private int speed = 1;
    private int hori = 0;
    private int verti = 0;

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getHori() {
        return hori;
    }

    public void setHori(int hori) {
        this.hori = hori;
    }

    public int getVerti() {
        return verti;
    }

    public void setVerti(int verti) {
        this.verti = verti;
    }
}
